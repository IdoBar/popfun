#!/usr/bin/env python3

import os
import sys
import tempfile


def usage() -> None:
    print(
        "usage: <bamtools_coverage_output {} fasta_index num_regions [coverage.tsv] >regions.bed".format(sys.argv[0])
    )
    print("Generates regions with even sequencing coverage, provided an input of coverage per-position as")
    print("generated by bamtools coverage.  In other words, generates regions such that the integral of")
    print("coverage is approximately equal for each.  These can be used when variant calling to reduce")
    print("variance in job runtime.")


def iter_coverage_rows(path):
    with open(path, encoding="utf-8") as handle:
        for raw_line in handle:
            line = raw_line.strip()
            if not line:
                continue
            chrom, pos, depth = line.split("\t")
            yield chrom, int(pos), int(depth)


if len(sys.argv) < 3:
    usage()
    raise SystemExit(1)

lengths = {}
with open(sys.argv[1], encoding="utf-8") as fai_handle:
    for line in fai_handle:
        chrom, length = line.split("\t")[:2]
        lengths[chrom] = int(length)

cleanup_path = None
coverage_path = sys.argv[3] if len(sys.argv) > 3 else None
if coverage_path is None:
    with tempfile.NamedTemporaryFile("w", encoding="utf-8", delete=False) as temp_handle:
        cleanup_path = temp_handle.name
        for raw_line in sys.stdin:
            temp_handle.write(raw_line)
    coverage_path = cleanup_path

try:
    total_coverage = 0
    row_count = 0
    for _chrom, _pos, depth in iter_coverage_rows(coverage_path):
        total_coverage += depth
        row_count += 1

    if row_count == 0 or total_coverage == 0:
        print("coverage_to_regions.py: no non-empty coverage rows were provided", file=sys.stderr)
        raise SystemExit(1)

    bp_per_region = total_coverage / int(sys.argv[2])

    lchrom = None
    lpos = 0
    bp_in_region = 0

    for chrom, pos, depth in iter_coverage_rows(coverage_path):
        if lchrom != chrom:
            if lchrom:
                print(lchrom + ":" + str(lpos) + "-" + str(lengths[lchrom]))
                lpos = 0
                lchrom = chrom
                bp_in_region = 0
            else:
                lchrom = chrom
        bp_in_region += depth
        if bp_in_region > bp_per_region:
            print(chrom + ":" + str(lpos) + "-" + str(pos))
            lpos = pos
            bp_in_region = 0

    print(lchrom + ":" + str(lpos) + "-" + str(lengths[lchrom]))
finally:
    if cleanup_path and os.path.exists(cleanup_path):
        os.unlink(cleanup_path)